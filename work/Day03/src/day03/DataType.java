package day03;

public class DataType {
	public static void main(String[] args) {
		
		// 정수형 변수
		int i = 100;
		long l = 1000;
		// 실수형 변수
		float f = 3.14f;
		double d = 3.14;
		// 문자형 변수
		char c = 'A';
		// 문자열 변수
		String s = "Hello";
		
		System.out.println(i);
		System.out.println(l);
		System.out.println(f);
		System.out.println(d);
		System.out.println(c);
		System.out.println(s);
		
	}
}
