package day04;

public class Inequlity_sign_operator {
	public static void main(String[] args) {
		
		int a = 10; // a변수에 10저장
		int b = 20; // b 변수에 20저장
		int c = 30; // c변수에 30저장
		
		// 항상 앞의 문자가 주어가 되어야함
		System.out.println(a < c); // a 가 c보다 작나?
		System.out.println(a > b); // a 가 b보다 크나?
		System.out.println(a + b <= c); // a + b가 c보다 작거나 같나?
		System.out.println(a + b >= c); // a + b가 c보다 크거나 같나?
		
	}
}
